package com.sfc.sf2.battle.mapcoords;

/**
 *
 * @author wiz
 */
public class BattleMapCoords {
    
    private int index;
    private int map;
    private int x;
    private int y;
    private int width;
    private int height;
    private int trigX;
    private int trigY;

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int getMap() {
        return map;
    }

    public void setMap(int map) {
        this.map = map;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getTrigX() {
        return trigX;
    }

    public void setTrigX(int trigX) {
        this.trigX = trigX;
    }

    public int getTrigY() {
        return trigY;
    }

    public void setTrigY(int trigY) {
        this.trigY = trigY;
    }

    
}
